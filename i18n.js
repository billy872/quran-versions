// Apply translations
function setLang(lang) {
  var t = T[lang];
  if (!t) return;
  // Set direction
  document.documentElement.setAttribute('dir', t.dir);
  document.documentElement.lang = lang === 'ur' ? 'ur' : lang === 'fa' ? 'fa' : lang === 'hi' ? 'hi' : 'en';
  // Update active button
  document.querySelectorAll('.bar-btn').forEach(function(b){ b.classList.remove('active'); });
  var idx = {en:0, hi:1, ur:2, fa:3};
  document.querySelectorAll('.bar-btn')[idx[lang]].classList.add('active');
  // Apply all translations
  document.querySelectorAll('[data-i]').forEach(function(el) {
    var key = el.getAttribute('data-i');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });
  // Save preference
  localStorage.setItem('lang', lang);
}

